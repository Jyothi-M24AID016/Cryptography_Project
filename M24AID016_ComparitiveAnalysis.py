def hash_file_chunked(filepath, algo='sha3_256'):
    """Hash a real file in 64KB chunks. Returns (hex_digest, MB/s)."""
    h=hashlib.new(algo); size=os.path.getsize(filepath)
    t0=time.perf_counter()
    with open(filepath,'rb') as f:
        for chunk in iter(lambda: f.read(65536), b''): h.update(chunk)
    elapsed=time.perf_counter()-t0
    return h.hexdigest(), round((size/1e6)/elapsed if elapsed>0 else 0, 3)



