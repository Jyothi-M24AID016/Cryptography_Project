def _analyze_variant(fn, sample, n_nist=400):
    """Full analysis of one variant on the given input sample.
    NIST bits are generated the same way as the code output: by hashing
    (input + counter) repeatedly, matching the user-input testing methodology."""
    digests = [fn(sample + i.to_bytes(4, 'little')) for i in range(n_nist)]
    bits = ''.join(to_bits(d) for d in digests)
    nist = {'Monobit': nist_monobit(bits), 'Runs': nist_runs(bits),
            'Block Freq': nist_block_freq(bits), 'Cumul Sums': nist_cumsum(bits)}
    passed = nist_pass(nist)
    entropy = round(float(np.mean([byte_entropy(d) for d in digests[:100]])), 3)
    bit_bal = round(bits.count('1') / len(bits) * 100, 2)
    main = fn(sample)
    # average avalanche over several single-bit flips for a stabler estimate
    avs = []
    for pos in range(min(16, len(sample) * 8)):
        ba = bytearray(sample) if sample else bytearray(b"\x00")
        ba[pos // 8] ^= (1 << (pos % 8))
        avs.append(avalanche_pct(main, fn(bytes(ba))))
    aval = round(float(np.mean(avs)), 3)
    return main, aval, entropy, bit_bal, nist, passed


def _time_hash(fn, size=20_000, reps=5):
    """Return (time_ms, MB/s) for hashing a fixed buffer. Best-of-reps for a
    stable, low-noise number so 'which is faster' is unambiguous."""
    data = os.urandom(size)
    times = []
    for _ in range(reps):
        t0 = time.perf_counter(); fn(data); times.append(time.perf_counter() - t0)
    best = min(times)
    ms = round(best * 1000, 3)
    mbs = round((size / 1e6) / best, 2) if best > 0 else 0.0
    return ms, mbs


def ui_hash(text, file_path):
    """Run the input through ALL variants (baseline + modified, incl. BLAKE3)
    and return a full metrics table: digest, throughput, avalanche, entropy,
    bit-balance, every NIST p-value, NIST pass count, and security level."""
    if file_path:
        with open(file_path, "rb") as f:
            data = f.read()
        src = f"file ({len(data):,} bytes)"
    else:
        data = (text or "IIT Jodhpur M.Tech AI - SHA3 vs BLAKE2 vs BLAKE3").encode()
        src = f"text ({len(data):,} bytes)"

    # Cap the sample so the pure-Python modified variants stay fast and digests
    # are directly comparable across variants.
    SAMPLE = data[:512]

    rows = []
    for name, cfg in VARIANTS.items():
        fn = cfg['fn']
        digest, aval, entropy, bit_bal, nist, passed = _analyze_variant(fn, SAMPLE)
        ms, mbs = _time_hash(fn, 20_000)   # explicit time + throughput, fixed buffer
        rows.append({
            'Variant': name,
            'Family': cfg['family'],
            'Goal': cfg['type'],
            'Digest (hex)': digest.hex()[:24] + '...',
            'Bytes': cfg['digest'],
            'Time (ms/20KB)': ms,
            'MB/s': mbs,
            'Avalanche %': aval,
            'Entropy': entropy,
            'Bit-bal %': bit_bal,
            'Monobit': nist['Monobit'],
            'Runs': nist['Runs'],
            'BlockFreq': nist['Block Freq'],
            'CumSum': nist['Cumul Sums'],
            'NIST': f"{passed}/4",
            'Security': security_label(cfg),
        })
    df = pd.DataFrame(rows)

    summary = (f"**Input:** {src}  \n"
               f"_Digest, avalanche, entropy, bit-balance and NIST p-values are computed on the "
               f"first {len(SAMPLE)} bytes of the input (kept small so the pure-Python modified "
               f"variants stay fast). NIST bits are generated by hashing input+counter, 400 times. "
               f"Throughput (MB/s) and Time (ms to hash a fixed 20 KB buffer, best of 5) are measured "
               f"for a fair speed comparison - lower Time / higher MB/s is faster. "
               f"A NIST p-value above 0.01 passes; ideal avalanche is ~50%, ideal bit-balance ~50%._")
    return summary, df


def ui_single_file_parallel(size_mb):
    """Demonstrate BLAKE3 single-file internal multithreading."""
    if not HAS_BLAKE3:
        return "BLAKE3 not installed. Run:  !pip install blake3 -q"
    size_mb = int(size_mb)
    big = os.urandom(size_mb * 1_000_000)

    def timed(threads):
        return min((lambda: (lambda s: (_blake3(big, max_threads=threads).digest(),
                                        time.perf_counter() - s)[1])(time.perf_counter()))()
                   for _ in range(3))
    t1 = timed(1); tA = timed(_blake3.AUTO)
    mbps1 = round(size_mb / t1, 1); mbpsA = round(size_mb / tA, 1)
    speedup = round(t1 / tA, 2) if tA > 0 else 1.0
    cores = multiprocessing.cpu_count()
    return (f"**BLAKE3 single-file multithreading ({size_mb} MB, {cores} core(s))**\n\n"
            f"| Mode | Time | Throughput |\n|---|---|---|\n"
            f"| 1 thread | {t1*1000:.1f} ms | {mbps1} MB/s |\n"
            f"| AUTO threads | {tA*1000:.1f} ms | {mbpsA} MB/s |\n\n"
            f"**Single-file speedup: {speedup}x**  \n"
            f"_SHA-3 and plain BLAKE2b cannot do this - a single hash is strictly sequential. "
            f"Speedup grows with the number of CPU cores (e.g. ~1.5-1.9x on a 2-core Colab runtime)._")


