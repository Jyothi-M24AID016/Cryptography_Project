def run_q3_mac():
    print("\n"+"="*62)
    print("Q3 -- BLAKE2 Native MAC vs HMAC-SHA3")
    print("="*62)
    print("  BLAKE2 has a built-in key parameter (native MAC).")
    print("  SHA-3 has no native key -- needs HMAC wrapper from Python hmac module.")

    key     = os.urandom(32)
    n       = 2000
    payloads= [os.urandom(random.choice([64,256,1024])) for _ in range(n)]

    # BLAKE2 native MAC
    t0=time.perf_counter()
    b2_macs=[hashlib.blake2b(p,key=key,digest_size=32).digest() for p in payloads]
    b2_t=time.perf_counter()-t0

    # HMAC-SHA3
    t0=time.perf_counter()
    sha3_macs=[hmac.new(key,p,digestmod=hashlib.sha3_256).digest() for p in payloads]
    sha3_t=time.perf_counter()-t0

    b2_us   = round(b2_t/n*1e6,2)
    sha3_us = round(sha3_t/n*1e6,2)
    speedup = round(sha3_t/b2_t,2)

    # BLAKE3 native keyed MAC (also keyed natively, no HMAC wrapper needed)
    b3_us=None; b3_speedup=None
    if HAS_BLAKE3:
        t0=time.perf_counter()
        b3_macs=[b3_keyed(p,key) for p in payloads]
        b3_t=time.perf_counter()-t0
        b3_us=round(b3_t/n*1e6,2)
        b3_speedup=round(sha3_t/b3_t,2)

    print(f"\n  Speed comparison ({n} operations, mixed 64/256/1024B payloads):")
    print(f"    BLAKE2 native MAC  : {b2_us} microseconds per operation")
    if HAS_BLAKE3:
        print(f"    BLAKE3 native MAC  : {b3_us} microseconds per operation")
    print(f"    HMAC-SHA3 wrapper  : {sha3_us} microseconds per operation")
    print(f"    BLAKE2 speedup     : {speedup}x faster than HMAC-SHA3")
    if HAS_BLAKE3:
        print(f"    BLAKE3 speedup     : {b3_speedup}x faster than HMAC-SHA3")

    # tamper detection test
    tampered=payloads[0][:-1]+bytes([payloads[0][-1]^0xFF])
    b2_detect  = b2_macs[0]   != hashlib.blake2b(tampered,key=key,digest_size=32).digest()
    sha3_detect= sha3_macs[0] != hmac.new(key,tampered,digestmod=hashlib.sha3_256).digest()
    b3_detect  = (b3_macs[0]  != b3_keyed(tampered,key)) if HAS_BLAKE3 else None
    print(f"\n  Tamper detection (1 byte changed in payload):")
    print(f"    BLAKE2 native MAC detects tampering : {b2_detect}")
    if HAS_BLAKE3:
        print(f"    BLAKE3 native MAC detects tampering : {b3_detect}")
    print(f"    HMAC-SHA3 detects tampering         : {sha3_detect}")
    print(f"    All detect equally well (as expected)")

    # security: key confusion test
    wrong_key=os.urandom(32)
    b2_wrong  = hashlib.blake2b(payloads[0],key=wrong_key,digest_size=32).digest()
    sha3_wrong= hmac.new(wrong_key,payloads[0],digestmod=hashlib.sha3_256).digest()
    print(f"\n  Wrong key produces different MAC:")
    print(f"    BLAKE2 native: {b2_macs[0].hex()[:24]}... != {b2_wrong.hex()[:24]}...")
    if HAS_BLAKE3:
        b3_wrong=b3_keyed(payloads[0],wrong_key)
        print(f"    BLAKE3 native: {b3_macs[0].hex()[:24]}... != {b3_wrong.hex()[:24]}...")
    print(f"    HMAC-SHA3    : {sha3_macs[0].hex()[:24]}... != {sha3_wrong.hex()[:24]}...")

    print(f"\n  Summary:")
    print(f"    BLAKE2 native MAC is {speedup}x faster because it has no wrapper overhead.")
    if HAS_BLAKE3:
        print(f"    BLAKE3 native MAC is {b3_speedup}x faster -- also keyed natively (no HMAC).")
    print(f"    HMAC-SHA3 is a two-layer construct: HMAC(SHA3(...)). Adds one hash call overhead.")
    print(f"    Security: all correctly detect tampering and reject wrong keys.")
    print(f"    Recommendation: a native keyed hash (BLAKE2/BLAKE3) for throughput-critical MACs.")

    out={'b2_us':b2_us,'sha3_us':sha3_us,'speedup':speedup,
         'b2_detect':b2_detect,'sha3_detect':sha3_detect}
    if HAS_BLAKE3:
        out.update({'b3_us':b3_us,'b3_speedup':b3_speedup,'b3_detect':b3_detect})
    return out



