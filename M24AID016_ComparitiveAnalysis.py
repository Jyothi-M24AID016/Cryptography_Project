def _ror64(v, n): return ((v >> n) | (v << (64-n))) & M64

def _b2G(v, a, b, c, d, x, y):
    v[a]=(v[a]+v[b]+x)&M64; v[d]=_ror64(v[d]^v[a],32)
    v[c]=(v[c]+v[d])  &M64; v[b]=_ror64(v[b]^v[c],24)
    v[a]=(v[a]+v[b]+y)&M64; v[d]=_ror64(v[d]^v[a],16)
    v[c]=(v[c]+v[d])  &M64; v[b]=_ror64(v[b]^v[c],63)

def _b2_compress(h, blk, t, last, rounds):
    m=[int.from_bytes(blk[i*8:(i+1)*8],'little') for i in range(16)]
    v=list(h)+list(BLAKE2B_IV)
    v[12]^=t&M64; v[13]^=(t>>64)&M64
    if last: v[14]^=M64
    for r in range(rounds):
        s=BLAKE2B_SIGMA[r%10]
        _b2G(v,0,4, 8,12,m[s[0]], m[s[1]]); _b2G(v,1,5, 9,13,m[s[2]], m[s[3]])
        _b2G(v,2,6,10,14,m[s[4]], m[s[5]]); _b2G(v,3,7,11,15,m[s[6]], m[s[7]])
        _b2G(v,0,5,10,15,m[s[8]], m[s[9]]); _b2G(v,1,6,11,12,m[s[10]],m[s[11]])
        _b2G(v,2,7, 8,13,m[s[12]],m[s[13]]); _b2G(v,3,4,9,14,m[s[14]],m[s[15]])
    for i in range(8): h[i]^=v[i]^v[i+8]

def blake2b_pure(data, digest_size=32, num_rounds=10):
    BLOCK=128; h=list(BLAKE2B_IV)
    h[0]^=(digest_size)|(1<<16)|(1<<24)
    n=len(data)
    if n==0: _b2_compress(h, bytes(BLOCK), 0, True, num_rounds)
    else:
        pos=0
        while pos<n:
            blk=data[pos:pos+BLOCK]; pos+=BLOCK
            blk=blk.ljust(BLOCK,b'\x00')
            _b2_compress(h, blk, min(pos,n), pos>=n, num_rounds)
    return b''.join(h[i].to_bytes(8,'little') for i in range(8))[:digest_size]



