def _rot64(v, n):
    return ((v << n) | (v >> (64 - n))) & M64

def keccak_f1600(state, num_rounds=24):
    A = list(state)
    for rc in KECCAK_RC[:num_rounds]:
        C = [A[x]^A[x+5]^A[x+10]^A[x+15]^A[x+20] for x in range(5)]
        D = [C[(x-1)%5] ^ _rot64(C[(x+1)%5], 1) for x in range(5)]
        A = [A[i] ^ D[i%5] for i in range(25)]
        B = [0]*25
        for x in range(5):
            for y in range(5):
                B[y+5*((2*x+3*y)%5)] = _rot64(A[x+5*y], KECCAK_ROT[x+5*y])
        A = [B[x+5*y]^((B[(x+1)%5+5*y]^M64)&B[(x+2)%5+5*y])
             for y in range(5) for x in range(5)]
        A[0] ^= rc
    return A

def sha3_pure(data, num_rounds=24, rate_bytes=136, digest_bytes=32):
    RATE = rate_bytes
    msg = bytearray(data) + bytearray([0x06])
    while len(msg) % RATE: msg.append(0x00)
    msg[-1] |= 0x80
    state = [0]*25
    for blk in range(0, len(msg), RATE):
        for i in range(RATE//8):
            state[i] ^= int.from_bytes(msg[blk+i*8:blk+i*8+8], 'little')
        state = keccak_f1600(state, num_rounds)
    out = b''
    while len(out) < digest_bytes:
        out += b''.join(state[i].to_bytes(8,'little') for i in range(RATE//8))
        if len(out) < digest_bytes: state = keccak_f1600(state, num_rounds)
    return out[:digest_bytes]



