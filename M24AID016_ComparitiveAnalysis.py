    data=[os.urandom(256) for _ in range(n)]
    max_cores=min(multiprocessing.cpu_count(),4)
    core_list=[c for c in [1,2,4] if c<=max_cores]
    rows=[]; base_s=base_b=None

    for cores in core_list:
        sz=n//cores
        chunks=[data[i*sz:(i+1)*sz] for i in range(cores)]

        t0=time.perf_counter()
        with multiprocessing.Pool(cores) as pool: pool.map(_sha3_worker,chunks)
        sha3_t=round(time.perf_counter()-t0,4)

        t0=time.perf_counter()
        with multiprocessing.Pool(cores) as pool: pool.map(_b2_worker,chunks)
        b2_t=round(time.perf_counter()-t0,4)

        if cores==1: base_s=sha3_t; base_b=b2_t
        sha3_sp=round(base_s/sha3_t,2); b2_sp=round(base_b/b2_t,2)
        rows.append({'Cores':cores,'SHA3 Time(s)':sha3_t,'BLAKE2 Time(s)':b2_t,
                     'SHA3 Speedup':sha3_sp,'BLAKE2 Speedup':b2_sp})
        print(f"  {cores} core(s) : SHA3={sha3_t:.4f}s (x{sha3_sp})  "
              f"BLAKE2={b2_t:.4f}s (x{b2_sp})")

    df=pd.DataFrame(rows)
    print(f"\n  Note: above, both algorithms parallelize similarly because we distribute")
    print(f"  independent inputs (not splitting a single file) across processes.")

    # --- BLAKE3 single-file internal multithreading -----------------------
    # This is what neither SHA-3 nor plain BLAKE2b can do: BLAKE3's Merkle-tree
    # mode hashes ONE large input across multiple threads internally.
    b3_single={}
    if HAS_BLAKE3:
        print("\n  -- BLAKE3 single-file multithreading (one large buffer) --")
        big=os.urandom(64_000_000)   # 64 MB single input
        def _time_b3(threads,reps=3):
            best=min((_timed(big,threads) for _ in range(reps)))
            return best
        def _timed(buf,threads):
            t0=time.perf_counter(); _blake3(buf,max_threads=threads).digest(); return time.perf_counter()-t0
        t1=_time_b3(1)
        tA=_time_b3(_blake3.AUTO)
        mbps1=round(64/t1,1); mbpsA=round(64/tA,1)
        speedup=round(t1/tA,2) if tA>0 else 1.0
        ncpu=multiprocessing.cpu_count()
        print(f"    64 MB, 1 thread     : {t1*1000:7.1f} ms  ({mbps1} MB/s)")
        print(f"    64 MB, AUTO threads : {tA*1000:7.1f} ms  ({mbpsA} MB/s)")
        print(f"    Single-file speedup : {speedup}x  (machine has {ncpu} core(s))")
        print(f"    SHA-3 / BLAKE2b cannot do this -- a single hash is strictly sequential.")
        b3_single={'mb':64,'t1_ms':round(t1*1000,1),'tA_ms':round(tA*1000,1),
                   'mbps1':mbps1,'mbpsA':mbpsA,'speedup':speedup,'cores':ncpu}
    else:
        print("\n  -- BLAKE3 single-file multithreading skipped (package missing) --")

    print(f"\n  Takeaway: for many independent messages, all three scale across processes.")
    print(f"  For ONE big file, only BLAKE3 parallelizes internally (BLAKE2bp would too).")
    return df, b3_single



