def to_bits(b):        return ''.join(f'{x:08b}' for x in b)
def byte_entropy(b):
    c=Counter(b); t=len(b)
    return -sum((v/t)*log2(v/t) for v in c.values()) if t>1 else 0.0
def avalanche_pct(h1,h2):
    return round(sum(bin(x^y).count('1') for x,y in zip(h1,h2))/(len(h1)*8)*100,3)

def nist_monobit(bits):
    n=len(bits); s=sum(1 if b=='1' else -1 for b in bits)
    return round(float(math.erfc(abs(s)/math.sqrt(2*n))),6)
def nist_runs(bits):
    n=len(bits); pi=bits.count('1')/n
    if abs(pi-.5)>2/sqrt(n): return 0.0
    runs=1+sum(bits[i]!=bits[i-1] for i in range(1,n))
    exp=2*n*pi*(1-pi)+1; var=2*n*pi*(1-pi)*(2*n*pi*(1-pi)-1)/(n-1)
    return round(float(2*(1-stats.norm.cdf(abs((runs-exp)/sqrt(var))))),6)
def nist_block_freq(bits, block=128):
    n=len(bits); nb=n//block
    if nb==0: return None
    chi=sum(((bits[i*block:(i+1)*block].count('1')-block/2)**2)*4/block for i in range(nb))
    return round(float(1-stats.chi2.cdf(chi,df=nb)),6)
def nist_cumsum(bits):
    x=np.array([1 if b=='1' else -1 for b in bits])
    return round(float(2*(1-stats.norm.cdf(np.max(np.abs(np.cumsum(x)))/sqrt(len(bits))))),6)
